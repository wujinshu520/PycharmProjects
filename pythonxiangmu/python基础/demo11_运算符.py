# 1、算术运算符
# +、-、*、/、%、//、**
a = 10
b = 2
print('运算结果：', a + b)
print('运算结果：', a - b)
print('运算结果：', a * b)
print('运算结果：', a / b)
print('运算结果：', a % b)
print('运算结果：', a // b)
print('运算结果：', a ** b)
print('-----------------------')

# 2、赋值运算符
# = 、+= 、-= 、*= 、/=、
c = 10
print('运算结果：', c)

c -= 5  # 相当于 c = c-5
print('运算结果：', c)

c += 6  # 相当于 c = c+6
print('运算结果：', c)
print('-----------------------')

# 3、关系运算符
